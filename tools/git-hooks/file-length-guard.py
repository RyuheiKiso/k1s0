#!/usr/bin/env python3
"""1 ファイル 500 行以内の規約を保証する pre-commit hook。

src/CLAUDE.md 規約「1 ファイルあたりの行数は 500 行以内とすること。いかなる例外も認めない」の
自動化版。docs 配下の md は適用外（規約も docs 例外を明示）。

対象（500 行ガード）:
    src/        コード全般
    infra/      yaml / 設定
    deploy/     yaml / 設定
    tools/      シェル / Python ツール
    tests/      テストコード

対象外:
    docs/       規約上例外
    plan/       計画文書（テンプレ）
    examples/   採用側雛形（個別調整余地）
    .github/    actions yaml
    自動生成    `Code generated` / `@generated` / `DO NOT EDIT` 等を含む
    lock        *.lock / package-lock.json / Cargo.lock 等

Usage:
    tools/git-hooks/file-length-guard.py [<file> ...] [--limit 500]
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

DEFAULT_LIMIT = 500

# ガード対象のトップレベルディレクトリ
TARGET_PREFIXES = ("src/", "infra/", "deploy/", "tools/", "tests/")

# 適用対象外（docs は規約上例外）
EXEMPT_PREFIXES = ("docs/", "plan/", "examples/", ".github/", "third_party/")

# 拡張子で除外（lock / バイナリ / 生成物）
EXEMPT_SUFFIXES = (
    ".lock",
    ".sum",
    ".min.js",
    ".min.css",
    ".map",
    ".svg",
    ".drawio",
    ".png",
    ".jpg",
    ".jpeg",
    ".gif",
    ".ico",
    ".pdf",
    ".zip",
    ".tar",
    ".gz",
)

# ファイル名で除外
EXEMPT_FILENAMES = (
    "package-lock.json",
    "pnpm-lock.yaml",
    "yarn.lock",
    "Cargo.lock",
    "go.sum",
    "buf.lock",
)

# 自動生成マーカー（先頭 5 行に出現したらスキップ）
GENERATED_MARKERS = (
    "Code generated",
    "DO NOT EDIT",
    "@generated",
    "DO NOT MODIFY",
    "AUTOGENERATED",
)


def is_target(path: Path) -> bool:
    posix = path.as_posix()
    if any(posix.startswith(p) for p in EXEMPT_PREFIXES):
        return False
    if not any(posix.startswith(p) for p in TARGET_PREFIXES):
        return False
    if path.name in EXEMPT_FILENAMES:
        return False
    if any(path.name.endswith(s) for s in EXEMPT_SUFFIXES):
        return False
    return True


def is_generated(path: Path) -> bool:
    try:
        with path.open("r", encoding="utf-8", errors="replace") as f:
            head = "".join(next(f, "") for _ in range(5))
    except Exception:
        return False
    return any(m in head for m in GENERATED_MARKERS)


def count_lines(path: Path) -> int:
    try:
        with path.open("rb") as f:
            return sum(1 for _ in f)
    except Exception:
        return 0


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Enforce 500-line limit on src/infra/deploy/tools/tests files."
    )
    parser.add_argument("files", nargs="*", help="files to check")
    parser.add_argument("--limit", type=int, default=DEFAULT_LIMIT, help="max lines per file")
    parser.add_argument("--quiet", action="store_true")
    args = parser.parse_args()

    if not args.files:
        return 0

    violations: list[tuple[Path, int]] = []
    skipped = 0
    checked = 0
    for f in args.files:
        path = Path(f)
        if not path.is_file():
            continue
        if not is_target(path):
            skipped += 1
            continue
        if is_generated(path):
            skipped += 1
            continue
        n = count_lines(path)
        checked += 1
        if n > args.limit:
            violations.append((path, n))

    if violations:
        print(f"[file-length-guard] 500 行制限違反 ({len(violations)} 件):")
        for path, n in violations:
            print(f"  - {path.as_posix()}: {n} 行 (上限 {args.limit})")
        print(
            "\n対処: src/CLAUDE.md の 500 行規約に従い、ファイルを論理単位で分割してください。"
            "\n例外は認められません（docs 配下は適用外）。"
        )
        return 1

    if not args.quiet:
        print(f"[file-length-guard] OK (checked={checked}, skipped={skipped})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
