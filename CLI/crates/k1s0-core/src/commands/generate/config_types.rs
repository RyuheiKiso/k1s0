    use super::*;
    use crate::commands::validate::config_schema::{CategoryYaml, FieldYaml};
    use mockito::Matcher;
    use tempfile::{tempdir, NamedTempFile};

    fn sample_schema() -> ConfigSchemaYaml {
        ConfigSchemaYaml {
            version: 1,
            service: "my-service".to_string(),
            namespace_prefix: "service.myservice".to_string(),
            categories: vec![CategoryYaml {
                id: "general".to_string(),
                label: "General".to_string(),
                icon: Some("settings".to_string()),
                namespaces: vec!["service.myservice.general".to_string()],
                fields: vec![
                    FieldYaml {
                        key: "enable_feature".to_string(),
                        label: "Enable Feature".to_string(),
                        description: None,
                        field_type: Some("boolean".to_string()),
                        min: None,
                        max: None,
                        options: None,
                        pattern: None,
                        unit: None,
                        default: Some(serde_yaml::Value::Bool(false)),
                    },
                    FieldYaml {
                        key: "max_connections".to_string(),
                        label: "Max Connections".to_string(),
                        description: None,
                        field_type: Some("integer".to_string()),
                        min: Some(1.0),
                        max: Some(100.0),
                        options: None,
                        pattern: None,
                        unit: None,
                        default: Some(serde_yaml::Value::Number(serde_yaml::Number::from(10))),
                    },
                    FieldYaml {
                        key: "ssl_mode".to_string(),
                        label: "SSL Mode".to_string(),
                        description: None,
                        field_type: Some("enum".to_string()),
                        min: None,
                        max: None,
                        options: Some(vec![
                            "disable".to_string(),
                            "require".to_string(),
                            "verify-ca".to_string(),
                            "verify-full".to_string(),
                        ]),
                        pattern: None,
                        unit: None,
                        default: Some(serde_yaml::Value::String("require".to_string())),
                    },
                ],
            }],
        }
    }

    fn write_schema_file(content: &str) -> NamedTempFile {
        use std::io::Write;

        let mut file = NamedTempFile::new().unwrap();
        file.write_all(content.as_bytes()).unwrap();
        file
    }

    #[test]
    fn test_generate_typescript_types() {
        let schema = sample_schema();
        let ts = generate_typescript_types(&schema);

        assert!(ts.contains("export const ConfigKeys"));
        assert!(ts.contains("GENERAL: {"));
        assert!(ts.contains("ENABLE_FEATURE: 'enable_feature'"));
        assert!(ts.contains("'general.enable_feature': boolean"));
        assert!(ts.contains("'general.max_connections': number"));
        assert!(ts.contains("\"verify-ca\" | \"verify-full\""));
    }

    #[test]
    fn test_generate_dart_types() {
        let schema = sample_schema();
        let dart = generate_dart_types(&schema);

        assert!(dart.contains("enum GeneralConfigKey {"));
        assert!(dart.contains("  enableFeature,"));
        assert!(dart.contains("  maxConnections,"));
        assert!(dart.contains("  sslMode;"));
        assert!(dart.contains("GeneralConfigKey.enableFeature => 'enable_feature'"));
        assert!(dart.contains("GeneralConfigKey.sslMode => 'ssl_mode'"));
        assert!(dart.contains("enum SslMode { disable, require, verifyCa, verifyFull }"));
    }

    #[test]
    fn test_build_push_request() {
        let schema = sample_schema();
        let (method, url, headers, body) =
            build_push_request(&schema, "https://config.example.com", "test-token").unwrap();

        assert_eq!(method, "PUT");
        assert_eq!(
            url,
            "https://config.example.com/api/v1/config-schema/my-service"
        );
        assert!(headers
            .iter()
            .any(|(key, value)| key == "Authorization" && value == "Bearer test-token"));
        assert!(headers
            .iter()
            .any(|(key, value)| key == "Content-Type" && value == "application/json"));

        let body_json: serde_json::Value = serde_json::from_str(&body).unwrap();
        assert_eq!(body_json["namespace_prefix"], "service.myservice");
        assert!(body_json.get("service").is_none());
        assert_eq!(body_json["categories"][0]["fields"][2]["type"], "enum");
    }

    #[test]
    fn test_push_config_schema() {
        let schema = sample_schema();
        let mut server = mockito::Server::new();
        let mock = server
            .mock("PUT", "/api/v1/config-schema/my-service")
            .match_header("authorization", "Bearer test-token")
            .match_header("content-type", "application/json")
            .match_body(Matcher::PartialJson(serde_json::json!({
                "namespace_prefix": "service.myservice",
                "categories": [{
                    "id": "general"
                }]
            })))
            .with_status(200)
            .with_header("content-type", "application/json")
            .with_body(
                serde_json::json!({
                    "service": "my-service",
                    "namespace_prefix": "service.myservice",
                    "categories": []
                })
                .to_string(),
            )
            .create();

        push_config_schema(&schema, &server.url(), "test-token").unwrap();
        mock.assert();
    }

    #[test]
    fn test_write_generated_types_to_targets_from_file() {
        let schema_file = write_schema_file(
            r#"
version: 1
service: my-service
namespace_prefix: service.myservice
categories:
  - id: general
    label: General
    namespaces:
      - service.myservice.general
    fields:
      - key: flag
        label: Flag
        type: boolean
        default: false
"#,
        );
        let temp = tempdir().unwrap();
        let ts_dir = temp.path().join("react");
        let dart_dir = temp.path().join("flutter");
        let targets = vec![
            GeneratedTypesTarget {
                target: "typescript",
                output_dir: ts_dir.as_path(),
            },
            GeneratedTypesTarget {
                target: "dart",
                output_dir: dart_dir.as_path(),
            },
        ];

        let written =
            write_generated_types_to_targets_from_file(schema_file.path(), &targets).unwrap();

        assert_eq!(written.len(), 2);
        assert!(written.iter().any(|path| path.ends_with("config-types.ts")));
        assert!(written
            .iter()
            .any(|path| path.ends_with("config_types.dart")));
        assert!(ts_dir.join("config-types.ts").exists());
        assert!(dart_dir.join("config_types.dart").exists());
    }

