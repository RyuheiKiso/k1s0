// util パッケージはアプリケーション全体で再利用される汎用ユーティリティを提供する。
// セキュリティ関連のマスキング処理など、複数パッケージから共通利用される関数を集約する。
package util

// MaskSessionID はセッション ID を先頭 8 文字 + "..." にマスクして返す（L-5/HIGH-7 監査対応）。
// セッション ID をそのままログに出力するとセッション固定化攻撃やログ漏洩のリスクがある。
// ログ・エラーレスポンスには必ずこの関数を通した値を使用すること。
func MaskSessionID(id string) string {
	if len(id) <= 8 {
		return "..."
	}
	return id[:8] + "..."
}
